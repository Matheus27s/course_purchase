class User {
  String _name = "";
  String _email = "";
  String _password = "";

  String get name {
    return _name;
  }

  set name(String value) {
    _name = name;
  }

  String get email {
    return _email;
  }

  set email(String value) {
    _email = email;
  }

  String get password {
    return _password;
  }

  set password(String value) {
    _password = password;
  }
}