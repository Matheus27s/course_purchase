class UserModel {
  String _name = "";
  String _email = "";
  String _password = "";

  String get name {
    return _name;
  }

  set name(String value) {
    _name = value;
  }

  String get email {
    return _email;
  }

  set email(String value) {
    _email = value;
  }

  String get password {
    return _password;
  }

  set password(String value) {
    _password = value;
  }
}





